/*--------------------------------------------------------------------------------------------------------------------*/

import twigTaskMonitoringApp from './assets/twig/TaskMonitoringApp.twig';

/*--------------------------------------------------------------------------------------------------------------------*/

$AMIClass('TaskMonitoringApp', {
	/*----------------------------------------------------------------------------------------------------------------*/

	$extends: ami.SubApp,

	/*----------------------------------------------------------------------------------------------------------------*/

	$init: function()
	{
		this.$super.$init();
	},

	/*----------------------------------------------------------------------------------------------------------------*/

	onReady: function(userdata)
	{
		return amiWebApp.replaceHTML('#ami_main_content', twigTaskMonitoringApp);
	},

	/*----------------------------------------------------------------------------------------------------------------*/

	onLogin: function()
	{
		return this.mount();
	},

	/*----------------------------------------------------------------------------------------------------------------*/

	onLogout: function()
	{
		return this.mount();
	},

	/*----------------------------------------------------------------------------------------------------------------*/

	mount: function()
	{
		window.ami.mount('#ami_main_content');
	},

	/*----------------------------------------------------------------------------------------------------------------*/
});

/*--------------------------------------------------------------------------------------------------------------------*/
/* GLOBAL INSTANCE                                                                                                    */
/*--------------------------------------------------------------------------------------------------------------------*/

window.taskMonitoringApp = new TaskMonitoringApp();

