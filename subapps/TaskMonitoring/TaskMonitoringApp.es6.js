import twigTaskMonitoringApp from './assets/twig/TaskMonitoringApp.twig';

/*--------------------------------------------------------------------------------------------------------------------*/

$AMIClass('TaskMonitoringApp', {
	/*----------------------------------------------------------------------------------------------------------------*/

	$extends: ami.SubApp,

	/*----------------------------------------------------------------------------------------------------------------*/

	$init: function()
	{
		this.$super.$init();
	},

	/*----------------------------------------------------------------------------------------------------------------*/

	onReady: function(userdata)
	{
		const result = $.Deferred();

		amiWebApp.replaceHTML('#ami_main_content', twigTaskMonitoringApp).done(() => {

			this.isMounted = false;

			result.resolve();
		});

		return result;
	},

	/*----------------------------------------------------------------------------------------------------------------*/

	onLogin: function()
	{
		this.mount();
	},

	/*----------------------------------------------------------------------------------------------------------------*/

	onLogout: function()
	{
		this.mount();
	},

	/*----------------------------------------------------------------------------------------------------------------*/

	mount: function()
	{
		setTimeout(() => {

			if(!this.isMounted)
			{
				window.ami.mount('#task_monitoring');

				this.isMounted = true;
			}
		}, 500)
	},

	/*----------------------------------------------------------------------------------------------------------------*/
});

/*--------------------------------------------------------------------------------------------------------------------*/
/* GLOBAL INSTANCE                                                                                                    */
/*--------------------------------------------------------------------------------------------------------------------*/

window.taskMonitoringApp = new TaskMonitoringApp();

